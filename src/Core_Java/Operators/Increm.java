package Core_Java.Operators;

import java.util.Scanner;

public class Increm {
    public static void main(String[] args) {

        Scanner L= new Scanner(System.in);
        System.out.println("Enter any number");

        int F =L.nextInt();
        int R =1;
        while(5>=R){
            System.out.println(F);
            F+=10;
        }

    }

}
