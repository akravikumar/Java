package Core_Java.Operators;

public class Comparison_Operators {
    public static void main(String[] args) {

        int ravi =10;
        int b=20;

        //Statements correct
        System.out.println(ravi<b);
        System.out.println(ravi>b);
        System.out.println(ravi==b);
        System.out.println(ravi<=b);
        System.out.println(ravi>=b);




    }


}
