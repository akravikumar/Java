package Core_Java.ControlStatements.Loops;

public class While_loop {
    public static void main(String[] args) {
        int prasad = 70;
        int ram = 50;

        while (prasad>ram){

            System.out.println("Hello Prasad" );
            ram++;
            prasad--;
        }
        System.out.println(ram);
    }

}
